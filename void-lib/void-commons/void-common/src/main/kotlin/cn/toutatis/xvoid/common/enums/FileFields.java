package cn.toutatis.xvoid.common.enums;

/**
 * 文件可返回的标准字段
 * @author Toutatis_Gc
 */
public enum FileFields {

    /**
     * 原文件名
     */
    FIELD_ORIGIN_NAME,

    /**
     * 命名文件名
     */
    FIELD_FILE_NAME,

    /**
     * 文件大小
     */
    FIELD_FILE_SIZE,

    /**
     * 绝对路径
     */
    FIELD_URL,

    /**
     * 相对路径
     */
    FIELD_URI,

    /**
     * 返回字段是否有序
     */
    FEATURE_ORDER,
    ;

}
